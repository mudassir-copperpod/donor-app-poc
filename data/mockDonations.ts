export const getDonationById = (donationId: string): DonationRecord | undefined => {
  return mockDonations.find(donation => donation.donationId === donationId);
};

export const getDonationsByPetId = (petId: string): DonationRecord[] => {
  return mockDonations.filter(donation => donation.petId === petId)
    .sort((a, b) => b.donationDate.localeCompare(a.donationDate));
};

export const getDonationsByAppointmentId = (appointmentId: string): DonationRecord | undefined => {
  return mockDonations.find(donation => donation.appointmentId === appointmentId);
};

export const getRecentDonations = (limit: number = 10): DonationRecord[] => {
  return [...mockDonations]
    .sort((a, b) => b.donationDate.localeCompare(a.donationDate))
    .slice(0, limit);
};

export const getDonationStats = () => {
  const totalDonations = mockDonations.length;
  const totalVolume = mockDonations.reduce((sum, d) => sum + d.collectionDetails.volumeCollected, 0);
  const acceptedDonations = mockDonations.filter(d => d.donationStatus === DonationStatus.ACCEPTED).length;
  const usedBlood = mockDonations.filter(d => d.usageStatus === UsageStatus.USED).length;
  
  return {
    totalDonations,
    totalVolume,
    acceptedDonations,
    usedBlood,
    acceptanceRate: (acceptedDonations / totalDonations) * 100,
    usageRate: (usedBlood / totalDonations) * 100,
  };
};
